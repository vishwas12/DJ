package com.dj.dto;

import java.util.Date;

public class BookingHistory {
	private int bookingHistoryId;
	private int bookedUserId;
	private int userType;
	private int status;
	private Date bookingStartDate;
	private Date bookingEndDate;
	private int userId;
	public int getBookingHistoryId() {
		return bookingHistoryId;
	}
	public void setBookingHistoryId(int bookingHistoryId) {
		this.bookingHistoryId = bookingHistoryId;
	}
	public int getBookedUserId() {
		return bookedUserId;
	}
	public void setBookedUserId(int bookedUserId) {
		this.bookedUserId = bookedUserId;
	}
	public int getUserType() {
		return userType;
	}
	public void setUserType(int userType) {
		this.userType = userType;
	}
	public int getStatus() {
		return status;
	}
	public void setStatus(int status) {
		this.status = status;
	}
	public Date getBookingStartDate() {
		return bookingStartDate;
	}
	public void setBookingStartDate(Date bookingStartDate) {
		this.bookingStartDate = bookingStartDate;
	}
	public Date getBookingEndDate() {
		return bookingEndDate;
	}
	public void setBookingEndDate(Date bookingEndDate) {
		this.bookingEndDate = bookingEndDate;
	}
	public int getUserId() {
		return userId;
	}
	public void setUserId(int userId) {
		this.userId = userId;
	}
	
	
}
